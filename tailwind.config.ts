// Função para adicionar variáveis de cores
function addVariablesForColors({ addBase, theme }: any) {
  let allColors = flattenColorPalette(theme("colors"));
  let newVars = Object.fromEntries(
    Object.entries(allColors).map(([key, val]) => [`--${key}`, val])
  );

  addBase({
    ":root": newVars,
  });
}

// Função para achatar a paleta de cores
function flattenColorPalette(colors: any) {
  const result: Record<string, string> = {};

  const flattenColors = (colorObj: any, prefix = '') => {
    for (const key in colorObj) {
      const value = colorObj[key];
      const prefixedKey = prefix ? `${prefix}-${key}` : key;

      if (typeof value === 'string') {
        result[prefixedKey] = value;
      } else {
        flattenColors(value, prefixedKey);
      }
    }
  };

  flattenColors(colors);
  return result;
}

